package imagemagick.core.enums

/**
 * Specifies the interlace types.
 */
enum class Interlace {
    /** Undefined */
    UNDEFINED,
    /** NoInterlace */
    NOINTERLACE,
    /** Line */
    LINE,
    /** Plane */
    PLANE,
    /** Partition */
    PARTITION,
    /** Gif */
    GIF,
    /** Jpeg */
    JPEG,
    /** Png */
    Png,
}
