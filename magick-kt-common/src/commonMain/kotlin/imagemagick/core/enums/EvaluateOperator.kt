/**
 * Specifies the evaluate operator.
 */
public enum class EvaluateOperator {
    /** Undefined. */
    UNDEFINED,

    /** Abs. */
    ABS,

    /** Add. */
    ADD,

    /** Add modulus. */
    ADD_MODULUS,

    /** And. */
    AND,

    /** Cosine. */
    COSINE,

    /** Divide. */
    DIVIDE,

    /** Exponential. */
    EXPONENTIAL,

    /** Gaussian noise. */
    GAUSSIAN_NOISE,

    /** Impulse noise. */
    IMPULSE_NOISE,

    /** Laplacian noise. */
    LAPLACIAN_NOISE,

    /** Left shift. */
    LEFT_SHIFT,

    /** Log. */
    LOG,

    /** Max. */
    MAX,

    /** Mean. */
    MEAN,

    /** Median. */
    MEDIAN,

    /** Min. */
    MIN,

    /** Multiplicative noise. */
    MULTIPLICATIVE_NOISE,

    /** Multiply. */
    MULTIPLY,

    /** Or. */
    OR,

    /** Poisson noise. */
    POISSON_NOISE,

    /** Pow. */
    POW,

    /** Right shift. */
    RIGHT_SHIFT,

    /** Root mean square. */
    ROOT_MEAN_SQUARE,

    /** Set. */
    SET,

    /** Sine. */
    SINE,

    /** Subtract. */
    SUBTRACT,

    /** Sum. */
    SUM,

    /** Threshold black. */
    THRESHOLD_BLACK,

    /** Threshold. */
    THRESHOLD,

    /** Threshold white. */
    THRESHOLD_WHITE,

    /** Uniform noise. */
    UNIFORM_NOISE,

    /** Xor. */
    XOR,

    /** Inverse log. */
    INVERSE_LOG,
}
